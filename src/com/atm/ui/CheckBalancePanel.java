    public CheckBalancePanel(String accNum) {
    this.currentAccountNumber = accNum;
    
    // 1. Initialize components first (Crucial to avoid NullPointerException)
    initComponents();
    
    // 2. Initial UI Setup (Styling for high contrast)
    pbAtmLimit.setForeground(new java.awt.Color(255, 255, 255)); 

    pbAtmLimit.setOpaque(true);
    pbAtmLimit.setStringPainted(false); // Hide default percentage text

    pbPosLimit.setForeground(new java.awt.Color(255, 255, 255));

    pbPosLimit.setOpaque(true);
    pbPosLimit.setStringPainted(false);
    
    populateSecurityNotice();

    // 3. Data Fetching from DAO
    com.atm.dao.AccountDAO accDAO = new com.atm.dao.AccountDAO();
    // Use getAccountSummary because it returns 'limit' and 'type'
    java.util.Map<String, Object> details = accDAO.getAccountSummary(currentAccountNumber);
    
    if (details != null && details.containsKey("balance") && details.containsKey("limit")) {
        // Retrieve values as Wrapper Objects to avoid primitive crash
        actualBalance = (Double) details.get("balance");
        String type = (String) details.get("type");
        Double totalLimit = (Double) details.get("limit");
        
        // Update Labels
        jLabel1.setText("ACCOUNT SUMMARY : " + type.toUpperCase() + " TYPE");
        lblAmountDisplay.setText("₹" + String.format("%.2f", actualBalance));
        
        // 4. Daily Limit Status Logic
        com.atm.dao.TransactionDAO transDAO = new com.atm.dao.TransactionDAO();
        double todaySpent = transDAO.getTodayWithdrawalTotal(currentAccountNumber);
        
        // Update ATM Progress Bar and Text
        pbAtmLimit.setMinimum(0);
        pbAtmLimit.setMaximum(totalLimit.intValue());
        pbAtmLimit.setValue((int) todaySpent);
        lblAtmLimitText.setText(String.format("₹%.2f / ₹%.2f", todaySpent, totalLimit));
        
        // Update POS Progress Bar (Static example for design)
        pbPosLimit.setMaximum(5000);
        pbPosLimit.setValue(0);
        lblPosLimitText.setText("₹0.00 / ₹5000.00");
        
    } else {
        // Fallback for missing account data
        lblAmountDisplay.setText("ACCOUNT ERROR");
        System.err.println("No DB data found for account: " + currentAccountNumber);
    }
    
    // 5. Trend Graph Logic
    try {
        com.atm.dao.TransactionDAO trendDAO = new com.atm.dao.TransactionDAO();
        List<Double> trendData = trendDAO.getBalanceTrend(currentAccountNumber);

        if (trendData != null && !trendData.isEmpty()) {
            TrendChart myChart = new TrendChart(trendData);
            
            // Layout management for the container
            trendContainer.setLayout(new BorderLayout()); 
            trendContainer.removeAll(); 
            trendContainer.add(myChart, BorderLayout.CENTER);
            
            // Refresh visuals
            trendContainer.revalidate();
            trendContainer.repaint();
        }
    } catch (Exception e) {
        System.err.println("Error loading trend graph: " + e.getMessage());
    }

    // 6. Interaction Setup
    lblHideToggle.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));
}

    
    private void toggleBalance() {
        if (!isHidden) {
            lblAmountDisplay.setText("********");
            lblHideToggle.setText("👁 Show Balance");
            isHidden = true;
        } else {
            lblAmountDisplay.setText("₹" + String.format("%.2f", actualBalance));
            lblHideToggle.setText("👁 Hide Balance");
            isHidden = false;
        }
    }
