    private void populateSecurityNotice() {
    com.atm.dao.AccountDAO accDAO = new com.atm.dao.AccountDAO();
    java.util.List<String> loginData = accDAO.getLastLogins(currentAccountNumber);

    // Reset labels first
    lbl1.setText("No recent activity");
    lbl2.setText("");
    lbl3.setText("");
    lbl4.setText("");

    // Populate based on available data
    if (loginData.size() >= 1) lbl1.setText("• Latest: " + loginData.get(0));
    if (loginData.size() >= 2) lbl2.setText("• Previous: " + loginData.get(1));
    if (loginData.size() >= 3) lbl3.setText("• History: " + loginData.get(2));
    if (loginData.size() >= 4) lbl4.setText("• History: " + loginData.get(3));
}
    private void btnPrintReceiptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrintReceiptActionPerformed
        // TODO add your handling code here:
        try {
        com.atm.dao.AccountDAO accDAO = new com.atm.dao.AccountDAO();
        java.util.Map<String, Object> details = accDAO.getAccountDetails(currentAccountNumber);

        com.atm.service.ReceiptService service = new com.atm.service.ReceiptService();
        String path = service.generateBalanceReceipt(currentAccountNumber, details);

        if (path != null) {
            // Show Success Message
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Receipt Saved to: " + path, 
                "Success", javax.swing.JOptionPane.INFORMATION_MESSAGE);

            // AUTO-OPEN THE PDF
            try {
                File pdfFile = new File(path);
                if (pdfFile.exists()) {
                    if (java.awt.Desktop.isDesktopSupported()) {
                        java.awt.Desktop.getDesktop().open(pdfFile);
                    } else {
                        System.out.println("AWT Desktop is not supported on this system.");
                    }
                }
            } catch (Exception ex) {
                System.err.println("Error opening PDF: " + ex.getMessage());
            }

        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Error generating PDF.", "Error", 0);
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnPrintReceiptActionPerformed

