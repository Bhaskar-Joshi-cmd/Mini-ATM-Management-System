    private void populateHeaderData(String accNum) {
        try {
            com.atm.dao.AccountDAO accDAO = new com.atm.dao.AccountDAO();
            Map<String, Object> details = accDAO.getAccountSummary(accNum);

            if (details != null && !details.isEmpty()) {
                String ownerName = details.get("name") != null ? (String) details.get("name") : "User";
                lblWelcome.setText("WELCOME, " + ownerName.toUpperCase());

                String maskedAcc = "ACCOUNT NUMBER: " +  accNum;
                lblAccountNumber.setText(maskedAcc);
            }
        } catch (Exception e) {
            System.err.println("Error populating header: " + e.getMessage());
            lblWelcome.setText("Welcome, Guest");
            lblAccountNumber.setText("A/C No: " + accNum);
        }
    }
    private void setScaledLogo() {
    try {
        // 1. Updated path to look for atm_logo.png
        java.net.URL imgUrl = getClass().getResource("/resources/atm_logo.png");
        
        if (imgUrl != null) {
            javax.swing.ImageIcon icon = new javax.swing.ImageIcon(imgUrl);
            
            // 2. Scale it to exactly 75x75 with smooth filtering
            java.awt.Image img = icon.getImage();
            java.awt.Image scaledImg = img.getScaledInstance(75, 75, java.awt.Image.SCALE_SMOOTH);
            
            // 3. Apply to label
            if (lblLogo != null) {
                lblLogo.setIcon(new javax.swing.ImageIcon(scaledImg));
                lblLogo.setText(""); // Clears any placeholder text like "jLabel1"
            }
        } else {
            System.err.println("Error: atm_logo.png not found in /com/atm/resources/");
            // Optional: Set a default text if image is missing
            lblLogo.setText("ATM LOGO");
        }
    } catch (Exception e) {
        System.err.println("Logo Scaling Error: " + e.getMessage());
        e.printStackTrace();
    }
}

    
