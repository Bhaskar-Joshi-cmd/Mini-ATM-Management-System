    private void lblMenuLogoutMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblMenuLogoutMouseClicked
        // TODO add your handling code here:
        // 1. Confirm with the user (Optional but professional)
    int confirm = javax.swing.JOptionPane.showConfirmDialog(this, 
            "Are you sure you want to logout?", 
            "Logout Confirmation", 
            javax.swing.JOptionPane.YES_NO_OPTION);
    
    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        // 2. Clear the session data
        this.currentAccountNumber = null;
        
        // 3. Open the Login Frame
        new LoginFrame().setVisible(true);
        
        // 4. Close (destroy) the current MainFrame
        this.dispose();
    }
    }//GEN-LAST:event_lblMenuLogoutMouseClicked

