    private void loadInitialData() {
        com.atm.dao.AccountDAO accDAO = new com.atm.dao.AccountDAO();
        currentBalance = accDAO.getBalance(accountNumber);
        lblCurrentBalance.setText(String.format("₹ %.2f", currentBalance));
        lblNewBalance.setText(String.format("₹ %.2f", currentBalance));
        lblLiveWithdraw.setText("₹ 0.00"); // This represents the deposit amount here
    }
    
    private void setupLiveImpact() {
        txtWithdrawAmount.getDocument().addDocumentListener(new javax.swing.event.DocumentListener() {
            public void changedUpdate(javax.swing.event.DocumentEvent e) { updateLabels(); }
            public void removeUpdate(javax.swing.event.DocumentEvent e) { updateLabels(); }
            public void insertUpdate(javax.swing.event.DocumentEvent e) { updateLabels(); }

            private void updateLabels() {
                try {
                    String text = txtWithdrawAmount.getText();
                    double amount = text.isEmpty() ? 0 : Double.parseDouble(text);
                    lblLiveWithdraw.setText(String.format("₹ %.2f", amount));
                    
                    // UPDATED FOR DEPOSIT: Add currentBalance + amount
                    double depositBalance = currentBalance + amount;
                    lblNewBalance.setText(String.format("₹ %.2f", depositBalance));
                    
                    // Always Green for Deposit
                    lblNewBalance.setForeground(new java.awt.Color(51, 153, 0)); 
                } catch (NumberFormatException e) {
                    lblLiveWithdraw.setText("Invalid Amount");
                }
            }
        });
    }
    
    private void setAmount(String amount) {
        txtWithdrawAmount.setText(amount);
    }
