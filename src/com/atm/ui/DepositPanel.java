    private void btnConfirmWithdrawActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmWithdrawActionPerformed
        // TODO add your handling code here:
        try {
            double amount = Double.parseDouble(txtWithdrawAmount.getText());
            
            // 1. Basic Validation
            if (amount <= 0) {
                JOptionPane.showMessageDialog(this, "Please enter a valid amount to deposit.");
                return;
            }

            com.atm.dao.TransactionDAO transDAO = new com.atm.dao.TransactionDAO();
            com.atm.dao.AccountDAO accDAO = new com.atm.dao.AccountDAO();
            
            // 2. Perform Deposit (Addition)
            double newBalance = currentBalance + amount;
            
            if (accDAO.updateBalance(accountNumber, newBalance)) {
                // IMPORTANT: Record as 'Deposit'
                transDAO.recordTransaction(accountNumber, "Deposit", amount, newBalance);
                
                JOptionPane.showMessageDialog(this, "Deposit Successful!");

                // --- RECEIPT LOGIC ---
                try {
                    com.atm.service.ReceiptService service = new com.atm.service.ReceiptService();
                    Map<String, Object> receiptData = new HashMap<>();
                    receiptData.put("amount", amount);
                    receiptData.put("new_balance", newBalance);
                    receiptData.put("type", "Deposit");

                   String path = service.generateTransactionReceipt(accountNumber, receiptData);

                    if (path != null) {
                        File pdfFile = new File(path);
                        if (pdfFile.exists() && Desktop.isDesktopSupported()) {
                            Desktop.getDesktop().open(pdfFile);
                        }
                    }
                } catch (Exception ex) {
                    System.err.println("Receipt Error: " + ex.getMessage());
                }

                loadInitialData(); // Refresh UI
                txtWithdrawAmount.setText("");
            }
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid numeric amount.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnConfirmWithdrawActionPerformed


